class Event {
  final int id;
  final String titulo;
  final String descrip;
  final String urlFoto;
  final String fecha;
  final String hora;
  final String createdAt;

  Event({
    required this.id,
    required this.titulo,
    required this.descrip,
    required this.urlFoto,
    required this.fecha,
    required this.hora,
    required this.createdAt,
  });
}
